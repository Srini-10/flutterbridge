#!/usr/bin/env node
// Generates `docs/guide/supported-widgets.md` from the generator's own widget map.
//
// ## Why this is generated and not written
//
// Until M5-E there was **no public list of what compiles**. `supportedWidgetNames()` produced one only
// inside a `BRG3001` message, so the only way to learn whether a widget was supported was to write it and
// be refused. That is a poor answer to the first question anybody evaluating a compiler asks.
//
// A hand-written list would answer it once and then drift â€” the map has grown in seven of the last nine
// milestones. So the document is generated from `WIDGET_MAP`, and `--check` fails CI when it no longer
// matches, exactly as the widget catalogs and UIR models already do (ADR-18's argument, applied to
// documentation: state a fact once, in the place that owns it).
//
// The catalog is a *different* number and deliberately so. `catalog/widgets/*.json` describes what the
// **analyzer** recognises structurally; `WIDGET_MAP` is what the **React generator** can render. A widget
// can be in the catalog and not the map â€” extraction understands it, this target cannot yet draw it â€” and
// that gap is a fact worth publishing rather than hiding.

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

// By path, not by specifier: this script runs from the repository root, which is not a package that
// depends on the generator. The built `dist` is what `pnpm run build` produces and what ships.
import { WIDGET_MAP } from '../packages/generators/react/dist/index.js';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const target = join(root, 'docs/guide/supported-widgets.md');

const supported = Object.keys(WIDGET_MAP).sort();

/** Every widget the analyzer has catalog metadata for, across all first-party catalogs. */
function catalogued() {
  const names = new Set();
  for (const file of ['material.json', 'gap.json']) {
    const catalog = JSON.parse(readFileSync(join(root, 'catalog/widgets', file), 'utf8'));
    for (const widget of catalog.widgets ?? []) names.add(widget.name);
  }
  return names;
}

const known = catalogued();
const recognisedOnly = [...known].filter((name) => !supported.includes(name)).sort();

const columns = 4;
const table = (names) => {
  const rows = [];
  for (let i = 0; i < names.length; i += columns) {
    const cells = names.slice(i, i + columns);
    while (cells.length < columns) cells.push('');
    rows.push(`| ${cells.map((name) => (name === '' ? '' : `\`${name}\``)).join(' | ')} |`);
  }
  // A markdown table needs a header row before the separator, even when the header is empty: without one
  // the separator renders as a literal `| --- |` line.
  return [`|${'   |'.repeat(columns)}`, `|${' --- |'.repeat(columns)}`, ...rows].join('\n');
};

const document = `# Supported widgets

<!-- GENERATED by tools/widget-support-doc.mjs. Do not edit â€” run \`pnpm run codegen\`. -->

**${supported.length} Flutter widgets** compile to React today. A widget not on this list is *refused* with
\`BRG3001\` rather than approximated â€” the compiler will not draw something it cannot draw faithfully.

Coverage against real applications is a different and more useful number: **56.8%** of every widget
instantiation across two real, unmodified Flutter apps (measured by \`tests/corpus.test.ts\`, not estimated).
A short list covers most real code because real code repeats itself.

## Rendered by the React generator

${table(supported)}

## Recognised by the analyzer, not yet rendered

These have catalog metadata â€” extraction understands their structure, slots and children â€” but the React
generator has no mapping, so a program using one is refused rather than approximated.

${recognisedOnly.length === 0 ? '_None: every catalogued widget has a mapping._' : table(recognisedOnly)}

## What "supported" does not mean

- **Not every parameter.** A widget can be supported while a particular parameter is not; that is
  \`BRG3017\`, and the message names the parameter.
- **Not every colour.** A widget that paints a Material role needs the program's theme to define a token
  for it (INV-20). \`ColorScheme.fromSeed(â€¦)\` derives all 46; literal theme colours generally do not, and
  the refusal is \`BRG3010\`.
- **Not layout equivalence.** Supported means it renders with the semantics the runtime kit implements. The
  authoritative visual comparison (ADR-12) is not part of this release.

## Unsupported Flutter features

Whole areas, rather than individual widgets. Each is refused with a diagnostic that names the missing
capability and the layer that owns it â€” never a generic failure, and never a suggestion that your Flutter
code is at fault.

| Area | Diagnostic | Status |
| --- | --- | --- |
| Imperative navigation â€” \`Navigator.push\` / \`pushNamed\` / \`pushReplacement\` | \`BRG3013\`, \`BRG3008\` | [ADR-0025](../adr/0025-the-navigation-model.md) D2 names the construct (\`logic.Navigate\`). Proposed, not implemented |
| \`Navigator.pop\` / \`popUntil\` / \`maybePop\` | \`BRG3013\` | same construct. A pop is *not* a route transition (Spec v2.4 Â§A17.3), so it carries no edge |
| Route overlays â€” \`showDialog\`, \`showModalBottomSheet\`, \`showMenu\` | \`BRG3013\` | the same blocker: an overlay pushes a \`Route\`, so it is a navigation to an inline destination |
| Messenger overlays â€” \`showSnackBar\`, \`ScaffoldMessenger\` | \`BRG3013\` | a *different* blocker â€” a snack bar is enqueued, not routed. No ADR models it yet |
| Route constructor arguments â€” \`home: Screen(id: â€¦)\` | \`BRG3018\` | the analyzer extracts them; \`app.Route\` has no field linking a route to them ([ADR-0025](../adr/0025-the-navigation-model.md) D1) |
| Routes computed at runtime â€” \`onGenerateRoute\` | \`BRG1304\` | the route table is not read from a function; the routes it builds are invisible to N11 |
| Gestures beyond taps | \`BRG3013\` | not modelled |
| Slivers | \`BRG3013\` | refused, never emulated |
| Custom painting (\`CustomPaint\`, \`Canvas\`) | \`BRG3013\` | not modelled |
| Explicit animation (\`AnimationController\`) | \`BRG3013\` | not modelled; implicit animations are supported |
| Platform channels, plugins with native code | â€” | out of scope |
| \`FutureBuilder\`/\`StreamBuilder\` | \`BRG3007\` | rendering a future needs loading and error branches, and inventing either is what a generator must not do |
| User-defined classes as values | \`BRG3002\` | no class emission yet |

Navigation is the largest of these, and what it costs a real application is measured rather than guessed â€”
see the [navigation model validation](../m6/m6d-navigation-model-validation.md), which counts every
navigation construct across 80 476 lines of two production Flutter apps.

See [troubleshooting](../troubleshooting.md) for what each diagnostic means and what to do about it.
`;

if (process.argv.includes('--check')) {
  const current = readFileSync(target, 'utf8');
  if (current !== document) {
    console.error('widget-support-doc: docs/guide/supported-widgets.md is out of date. Run `pnpm run codegen`.');
    process.exit(1);
  }
  console.log(`widget-support-doc: ${supported.length} widgets documented, matches WIDGET_MAP.`);
} else {
  writeFileSync(target, document);
  console.log(`widget-support-doc: wrote ${supported.length} supported, ${recognisedOnly.length} recognised-only.`);
}
